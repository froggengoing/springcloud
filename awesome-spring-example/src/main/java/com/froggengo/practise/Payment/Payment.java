package com.froggengo.practise.Payment;

public class Payment {
    int id;
    String serial;

    public int getId() {
        return id;
    }

    public Payment setId(int id) {
        this.id = id;
        return this;
    }

    public String getSerial() {
        return serial;
    }

    public Payment setSerial(String serial) {
        this.serial = serial;
        return this;
    }
}
